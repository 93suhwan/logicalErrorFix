template <class T, class V>
void _print(unordered_map<T, V> v) {
  cerr << "[ ";
  for (auto i : v) {
    _print(i);
    cerr << " ";
  }
  cerr << "]";
}
void solve() {
  int tt;
  cin >> tt;
  while (tt--) {
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++) {
      cin >> arr[i];
    }
    int ans = 0;
    vector<int> arr2 = arr;
    sort((arr2).begin(), (arr2).end());
    while (arr2 != arr) {
      for (int i = ans % 2; i + 1 < n; i += 2) {
        if (arr[i] > arr[i + 1]) swap(arr[i], arr[i + 1]);
      }
      ans++;
    }
    cout << ans << "\n";
  }
}
int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  cout.tie(NULL);
  solve();
}
