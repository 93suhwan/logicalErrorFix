template <class T>
void _print(list<T> v) {
  cerr << "[ ";
  for (T i : v) {
    _print(i);
    cerr << " ";
  }
  cerr << "]";
}
int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  cout.tie(NULL);
  int tc = 0, T;
  cin >> T;
  while (++tc <= T) {
    int N;
    cin >> N;
    int ans = 0;
    int root = sqrt(N);
    ans += root;
    map<int, bool> visited;
    for (int i = 1; i <= root; i++) {
      visited[i * i] = true;
    }
    int cubeRoot = cbrt(N);
    for (int i = 1; i <= cubeRoot; i++) {
      if (visited[i * i * i] == false) {
        ans++;
      }
    }
    cout << ans << endl;
  }
  return 0;
}
