bool check(int alpha) {
  string gamma;
  gamma = to_string(alpha);
  int l = gamma.length();
  if (gamma[l - 1] == '3') {
    return true;
  };
  return false;
}
int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  cout.tie(NULL);
  int t;
  cin >> t;
  vector<int> alpha;
  int count = 0;
  while (count <= 1700) {
    if ((count % 3 != 0) && check(count) == false) {
      alpha.push_back(count);
    }
    count++;
  };
  while (t--) {
    int gamma;
    cin >> gamma;
    cout << alpha[gamma - 1] << "\n";
  }
}
