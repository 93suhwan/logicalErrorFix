void solve() {
  int t;
  cin >> t;
  while (t--) {
    int n, k;
    cin >> n >> k;
    vector<int> arr(n);
    for (auto &x : arr) cin >> x;
    sort(arr.begin(), arr.end(), greater<int>());
    int sum = 0;
    for (int i = 0; i < k; i++) sum += arr[i + k] / arr[i];
    for (int i = 2 * k; i < n; i++) sum += arr[i];
    cout << sum << '\n';
  }
}
