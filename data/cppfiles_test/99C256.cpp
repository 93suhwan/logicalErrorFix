void solve() {
  long long x0, n;
  cin >> x0 >> n;
  long long rem = 4 * (n / 4);
  while (rem < n) {
    if (x0 % 2 == 0)
      x0 -= (rem + 1);
    else
      x0 += (rem + 1);
    rem++;
  }
  cout << x0 << "\n";
}
