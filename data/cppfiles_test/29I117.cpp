vector<long long> sieve(long long n) {
  vector<long long> arr(n + 1, true);
  vector<long long> vect;
  for (long long i = 2; i <= n; i++)
    if (arr[i] == 1) {
      vect.push_back(i);
      for (long long j = 2 * i; j <= n; j += i) arr[j] = 0;
    }
  return vect;
}
