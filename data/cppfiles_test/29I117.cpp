vector<long long> factor_(long long n) {
  vector<long long> factor;
  for (long long i = 1; i * i <= n; i++) {
    if (n % i == 0) {
      factor.push_back(i);
      if (i != n / i) factor.push_back(n / i);
    }
  }
  return factor;
}
