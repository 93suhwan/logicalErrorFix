bool IsPrime(long long number) {
  if (number <= 1) return false;
  if (number == 2 || number == 3) return true;
  if (number % 2 == 0 || number % 3 == 0) return false;
  for (long long i = 5; i * i <= number; i += 2) {
    if (number % i == 0) return false;
  }
  return true;
}
void solve() {
  long long l, r;
  cin >> l >> r;
  if (l * 2 <= r) {
    if (r % 2 == 0) {
      cout << (r - 1) - (r / 2) << "\n";
    } else {
      cout << ((r) - (r + 1) / 2) << "\n";
    }
  } else {
    cout << r - l << "\n";
  }
}
