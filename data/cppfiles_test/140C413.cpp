  int t;
  cin >> t;
  while (t--) {
    int n;
    cin >> n;
    int x1 = pow(n, 0.5);
    int x2 = cbrt(n);
    int ans = x1 + x2;
    int i = 1;
    int dif = 3;
    int cnt = 0;
    int val = i * i * i;
    while (val <= n) {
      cnt++;
      i = i + dif;
      dif = dif + 2;
      val = i * i * i;
    }
    cout << ans - cnt << endl;
  }
