const long long N = 1e9 + 7;
inline long long add(long long a, long long b) {
  return ((a % N) + (b % N)) % N;
}
inline long long sub(long long a, long long b) {
  return ((a % N) - (b % N) + N) % N;
}
inline long long mul(long long a, long long b) {
  return ((a % N) * (b % N)) % N;
}
